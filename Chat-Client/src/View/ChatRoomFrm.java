    private void btn_logoutroomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_logoutroomActionPerformed
        try {
            // TODO add your handling code here:
            MainRun.controller.write("LeaveRoom,"+idroom);
        } catch (IOException ex) {
            Logger.getLogger(ChatRoomFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
        for(int i=0;i<MainRun.user.getRoomsIn().size();i++){
            if(MainRun.user.getRoomsIn().get(i).getId()==idroom){
                MainRun.user.getRoomsIn().remove(i);
                break;
            }
        }
        this.dispose();
        Client.roomFrm = new RoomFrm();
        Client.roomFrm.setLocationRelativeTo(null);
        Client.roomFrm.setVisible(true);
    }//GEN-LAST:event_btn_logoutroomActionPerformed

    private void btn_searchmessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchmessActionPerformed
        // TODO add your handling code here:
        Area_searchmess.setText("");
        String mess = txt_searchmess.getText();
        txt_searchmess.setText("");
        for(int i=0;i<MainRun.user.SearchRoomById(idroom).getStringMessages().size();i++){
            if(MainRun.user.SearchRoomById(idroom).getStringMessages().get(i).getContent().contains(mess) || mess.contains(MainRun.user.SearchRoomById(idroom).getStringMessages().get(i).getContent())){
                 String s = MainRun.user.SearchRoomById(idroom).getStringMessages().get(i).getUserName()+" :"+MainRun.user.SearchRoomById(idroom).getStringMessages().get(i).getContent();
                    s = s+"\n";
                    Area_searchmess.append(s);
            }
        }
        Area_searchmess.setWrapStyleWord(true);
        Area_searchmess.setLineWrap(true);
        Area_searchmess.setEditable(false);
    }//GEN-LAST:event_btn_searchmessActionPerformed
    public void setTable(ArrayList<User> users) throws IOException{
         String[] columnNames = {"username"};
         String[][] value = new String[users.size()][1];
         for(int i=0;i<users.size();i++){
             value[i][0] = users.get(i).getUsername()+"";
         }
         DefaultTableModel tablemodel = new DefaultTableModel(value,columnNames){
         public boolean isCellEdittable(int row,int column){
             return false;
         }
         };
         table_user.setModel(tablemodel);
         table_user.setEditingColumn(0);
         table_user.setEditingRow(0);
    }
    public boolean  checkOnline(String username){
        boolean check = false;
        for(int i=0;i<MainRun.user.getFriends().size();i++){
            if(MainRun.user.getUsername().equals(username)) {check = MainRun.user.getIsOnline();break;}
        
        }
        return check;
    }
    public void fillmess(){
        for(int i=0;i<MainRun.user.getRoomsIn().size();i++){
            if(MainRun.user.getRoomsIn().get(i).getId()==idroom){
                for(int j=0;j<MainRun.user.getRoomsIn().get(i).getStringMessages().size();j++){
                    String s = MainRun.user.getRoomsIn().get(i).getStringMessages().get(j).getUserName()+" :"+MainRun.user.getRoomsIn().get(i).getStringMessages().get(j).getContent();
                    s = s+"\n";
                    Area_mess.append(s);
                }
            }
        }
        Area_mess.setWrapStyleWord(true);
        Area_mess.setLineWrap(true);
        Area_mess.setEditable(false);
    }
    public void addmess(StringMessage stringMessage){
        String s = stringMessage.getUserName()+" :"+stringMessage.getContent();
        Area_mess.append(s+"\n");
    }
    
