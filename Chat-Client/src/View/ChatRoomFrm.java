    private void btn_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sendActionPerformed
        // TODO add your handling code here:
        if(txt_send.getText()=="") JOptionPane.showMessageDialog(this,"vui long nhap tin nhan");
        else{
            int idserial = 0;
            for(int i=0;i<MainRun.user.getRoomsIn().size();i++){
            if(MainRun.user.getRoomsIn().get(i).getId()==idroom){
               if(MainRun.user.getRoomsIn().get(i).getStringMessages().size()!=0) {
                    idserial =  MainRun.user.getRoomsIn().get(i).getStringMessages().get(MainRun.user.getRoomsIn().get(i).getStringMessages().size()-1).getSerial()+1;
                    break;}
            }
            
            StringMessage stringMessage = new StringMessage();
            stringMessage.setUserName(MainRun.user.getUsername());
            stringMessage.setSerial(idserial);
            stringMessage.setContent(txt_send.getText());
            
            this.addmess(stringMessage);
            for(int j=0;j<MainRun.user.getRoomsIn().size();j++){
            if(MainRun.user.getRoomsIn().get(j).getId()==idroom){
                MainRun.user.getRoomsIn().get(j).getStringMessages().add(stringMessage);
                break;
            }
            }
            
            }
           
            try {
                MainRun.controller.write("Chat,"+idroom+","+idserial+","+txt_send.getText());
            } catch (IOException ex) {
                Logger.getLogger(ChatRoomFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
         txt_send.setText("");
    }//GEN-LAST:event_btn_sendActionPerformed

